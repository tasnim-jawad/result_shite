
function gread_bangla(number){

    if(number <0 || number > 100){

        document.getElementById("result_show_bangla").innerHTML ='bangla: input valide number'.toUpperCase();
        alert('Please Check Your Bangla subject Number')
     
    }else if(number >=33 && number < 40){

        document.getElementById("result_show_bangla").innerHTML ='bangla: d'.toUpperCase();

    }else if(number >=40 && number < 50){

        document.getElementById("result_show_bangla").innerHTML ='bangla: c'.toUpperCase();

    }else if(number >=50 && number < 60){

        document.getElementById("result_show_bangla").innerHTML ='bangla: b'.toUpperCase();

    }else if(number >=60 && number < 70){

        document.getElementById("result_show_bangla").innerHTML ='bangla: a-'.toUpperCase();

    }else if(number >=70 && number < 80){

        document.getElementById("result_show_bangla").innerHTML ='bangla: a'.toUpperCase();

    }else if(number >=80 && number < 100){

        document.getElementById("result_show_bangla").innerHTML ='bangla: a+'.toUpperCase();
    }else{

        document.getElementById("result_show_bangla").innerHTML ='you are fail in bangla'.toUpperCase();

    }
}

